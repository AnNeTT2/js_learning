// № 2 Сделайте функцию, которая параметром принимает число и проверяет, положительное это число или отрицательное. В первом случае пусть функция выводит в консоль текст '+++', а во втором '---'.

function func3(num) {
    if (num > 0) {
        console.log('+++');
    } else {
        console.log('---');
    }
    ;
};
func3(-8)