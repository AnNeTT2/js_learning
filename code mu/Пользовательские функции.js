// №2 Сделайте функцию, выводящую в консоль сумму чисел от 1 до 100.
function func1() {
    let sum = 0;
    for (let i = 0; i <= 100; i++) {
        sum += i;
        console.log(sum);
    }
    ;
};
func1()

